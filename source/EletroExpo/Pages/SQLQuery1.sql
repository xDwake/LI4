CREATE TABLE formularios (
	 idFormulario INT PRIMARY KEY IDENTITY, 
	 ramoAtividade VARCHAR(45) NOT NULL, 
	 morada VARCHAR(45) NOT NULL, 
	 contacto VARCHAR(45) NOT NULL,
	 nomeEmpresa VARCHAR(45) NOT NULL,
	 codPostal VARCHAR(45) NOT NULL
);

CREATE TABLE utilizadores (
	 idUtilizador INT PRIMARY KEY IDENTITY, 
	 username VARCHAR(45) NOT NULL UNIQUE, 
	 email VARCHAR(45) NOT NULL UNIQUE, 
	 password VARCHAR(45) NOT NULL,
	 isVendedor BIT NOT NULL,
	 isAtivo BIT NOT NULL,
	 isAdministrador BIT NOT NULL,
	 idFormulario INT FOREIGN KEY REFERENCES formularios (idFormulario)
);

CREATE TABLE produtos (
	idProduto INT NOT NULL PRIMARY KEY IDENTITY,
	marca VARCHAR(45) NOT NULL,
	modelo VARCHAR(45) NOT NULL,
	rating FLOAT(1) NOT NULL,
	especificacao VARCHAR(255) NOT NULL,
	categoria VARCHAR(45) NOT NULL,
	stock INT,
	preco FLOAT(2) NOT NULL,
	idFormulario INT FOREIGN KEY REFERENCES formularios (idFormulario)
);

